package com.msj.common.dto;

import com.alibaba.fastjson.annotation.JSONField;

import java.math.BigDecimal;

public class ShoppingCartDto {
    private Integer id;

    private Integer productId;

    private String name;

    private String image;

    private BigDecimal price;

    private BigDecimal originalPrice;

    private BigDecimal discountPrice;

    private Integer num;

    private Integer status;

    private Integer stock;

    @JSONField(serialize = false)
    private Integer shoppingCartStatus;

    @JSONField(serialize = false)
    private String remark;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getProductId() {
        return productId;
    }

    public void setProductId(Integer productId) {
        this.productId = productId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public BigDecimal getPrice() {
        return price;
    }

    public void setPrice(BigDecimal price) {
        this.price = price;
    }

    public BigDecimal getOriginalPrice() {
        return originalPrice;
    }

    public void setOriginalPrice(BigDecimal originalPrice) {
        this.originalPrice = originalPrice;
    }

    public BigDecimal getDiscountPrice() {
        return discountPrice;
    }

    public void setDiscountPrice(BigDecimal discountPrice) {
        this.discountPrice = discountPrice;
    }

    public Integer getNum() {
        return num;
    }

    public void setNum(Integer num) {
        this.num = num;
    }

    public Integer getStatus() {
        return status;
    }

    public void setStatus(Integer status) {
        this.status = status;
    }

    public Integer getStock() {
        return stock;
    }

    public void setStock(Integer stock) {
        this.stock = stock;
    }

    public Integer getShoppingCartStatus() {
        return shoppingCartStatus;
    }

    public void setShoppingCartStatus(Integer shoppingCartStatus) {
        this.shoppingCartStatus = shoppingCartStatus;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }
}
